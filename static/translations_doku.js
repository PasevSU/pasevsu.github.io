// Theme management
function toggleTheme() {
    const currentTheme = document.documentElement.getAttribute('data-theme');
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    
    document.documentElement.setAttribute('data-theme', newTheme);
    localStorage.setItem('theme', newTheme);
    
    // Update theme icon
    const themeIcon = document.querySelector('.theme-icon');
    themeIcon.textContent = newTheme === 'dark' ? '‚òÄÔ∏è' : 'üåô';
}

function initTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    document.documentElement.setAttribute('data-theme', savedTheme);
    
    // Set initial theme icon
    const themeIcon = document.querySelector('.theme-icon');
    themeIcon.textContent = savedTheme === 'dark' ? '‚òÄÔ∏è' : 'üåô';
}

class RepositoryManager {
    constructor() {
        this.currentLang = 'bg';
        this.init();
    }

    init() {
        this.setInitialLanguage();
        this.renderContent();
    }

    setInitialLanguage() {
        const urlParams = new URLSearchParams(window.location.search);
        const langParam = urlParams.get('lang');
        
        if (langParam && ['bg', 'en', 'de'].includes(langParam)) {
            this.currentLang = langParam;
        }
        
        this.updateLanguageButtons();
    }

    updateLanguageButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === this.currentLang) {
                btn.classList.add('active');
            }
        });
    }

    renderContent() {
        this.updatePageTitle();
        this.updateHeader();
        this.updateBackButton();
        this.renderRepositories();
        this.updateTipSection();
    }

    updatePageTitle() {
        document.title = repositoryTranslations[this.currentLang]['page.title'];
    }

    updateHeader() {
        const header = document.querySelector('.header-title');
        if (header) {
            header.textContent = repositoryTranslations[this.currentLang]['header.title'];
        }
        
        const description = document.querySelector('.repo-description');
        if (description) {
            description.textContent = repositoryTranslations[this.currentLang]['repo.description'];
        }
    }

    // Update back button text
    updateBackButton() {
        const backButton = document.querySelector('.back-button');
        if (backButton) {
            const newText = repositoryTranslations[this.currentLang]['back.button'];
            backButton.innerHTML = `<span class="back-icon">‚Üê</span> ${newText}`;
        }
    }

    renderRepositories() {
        const container = document.querySelector('.repositories-container');
        if (!container) return;

        let html = '';

        repositoryData.forEach(repo => {
            const translations = repositoryTranslations[this.currentLang];
            const titleKey = `repo.${repo.id}.title`;
            const descKey = `repo.${repo.id}.desc`;
            
            const title = translations[titleKey] || repo.id;
            const description = translations[descKey] || '';

            html += `
                <div class="repo-item">
                    <h3>${title}</h3>
                    <p><strong>Repo:</strong> <a href="${repo.url}" target="_blank" rel="noopener noreferrer">${repo.repo}</a></p>
                    <p><strong>Description:</strong> ${description}</p>
                    <a href="https://my.home-assistant.io/redirect/supervisor_add_addon_repository/?repository_url=${repo.url}" target="_blank" rel="noopener noreferrer">
                        <img src="https://my.home-assistant.io/badges/supervisor_add_addon_repository.svg" alt="ADD REPO" class="repo-button">
                    </a>
                </div>
            `;
        });

        container.innerHTML = html;
    }

    updateTipSection() {
        const tipBox = document.querySelector('.tip-box');
        if (!tipBox) return;

        const translations = repositoryTranslations[this.currentLang];
        
        tipBox.innerHTML = `
            <strong>${translations['tip.title']}</strong>
            <p>${this.parseMarkdownLinks(translations['tip.text'])}</p>
            <p>${translations['tip.contact']}</p>
        `;
    }

    parseMarkdownLinks(text) {
        return text.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener noreferrer">$1</a>');
    }

    changeLanguage(lang) {
        this.currentLang = lang;
        this.updateLanguageButtons();
        this.renderContent();
        
        // Update URL without reloading page
        const url = new URL(window.location);
        url.searchParams.set('lang', lang);
        window.history.replaceState({}, '', url);
    }
}

// Initialize repository manager
let repoManager;

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    initTheme(); // Initialize theme first
    repoManager = new RepositoryManager();
});

// Language change function
function changeLanguage(lang) {
    if (repoManager) {
        repoManager.changeLanguage(lang);
    }
}

// Keyboard shortcuts for language switching
document.addEventListener('keydown', function(e) {
    if (e.altKey) {
        switch(e.key) {
            case 'b':
                changeLanguage('bg');
                break;
            case 'e':
                changeLanguage('en');
                break;
            case 'd':
                changeLanguage('de');
                break;
        }
    }
});